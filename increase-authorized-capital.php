<p align="justify">The authorized capital represents the maximum number of shares a company is allowed to issue, which may be needed for future expansion during its business activities. To issue additional shares beyond the authorized capital limit, specific procedures must be followed.
Typically, the process involves filing documents with the Registrar of Companies (ROC) and organizing both a board meeting and a general meeting within the company. Despite its relatively small scope, this remains a pivotal stage in company management, as it enables the allocation of new shares, potentially impacting the company's ownership structure and capitalization.
Complying with legal requirements when increasing authorized capital is essential to maintain lawful operations and uphold strong corporate governance practices. Seeking expert advice and assistance to navigate this process can ensure the proper implementation of crucial steps in accordance with applicable laws and regulations.
</p><br>

<p align="justify">The procedure of Increased Authorized Capital:-</p><br>
<li><b>Authorization in the AOA:-</b>Authorization in the AOA:-In order to expand the capital, the firm must determine if the Articles of Association provide a provision for doing so. It is absolutely necessary for the provision to be authorised by the AOA. If that's not the case, the Companies Act of 2013 mandates that the article be changed.</li><br>
<li><b>Meeting of the Board of Directors:-</b> Before expanding the company's authorised capital, the BOD must examine a few issues. For instance, setting up an EGM to get the shareholders' permission and publishing a notice of the EGM along with an agenda and justifications.</li><br>
<li><b>MOA Alteration:-</b> If the allowed capital is increased, the MOA's provision must be changed.</li>
