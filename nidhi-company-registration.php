<h2>Documents Required for Nidhi Company formation</h2>
</div>
<div class="progress-content">
<li>Pan Card of the Directors / Shareholders</li>
<li>Aadhaar Card / Voter Card / Passport of Directors / Shareholders</li>
<li>Individual copies of photo</li>
<li>Bank Statement First & Current Month Transaction Page of Directors / Shareholders</li>
<li>If rented then the Rent Agreement.</li>
<li>If Owned Tax Bill of the Premises</li>
<li>Electric Bill of the Premises.</li>
<li>NOC From the landlord, if rented</li>
<br>
<p align="justify">Un-encumbered term deposits. Every Nidhi shall invest and continue to keep invested, in unencumbered term deposits with a Scheduled commercial bank (other than a co-operative bank or a regional rural bank), or post office deposits in its own name an amount which shall not be less than ten percent of the deposits outstanding at the close of business on the last working day of the second preceding month.</p><br>
<p align="justify">If the failure to comply with these rules, Nidhi shall not accept any further deposits from the commencement of the second financial year till it complies with the provisions contained in this rule, besides being liable for penal consequences as provided in the Act.</p>
