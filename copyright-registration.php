<p align="justify">Copyright is the legal entitlement granted to creators of music, art, literature, movies, and software. It safeguards and incentivizes their creative endeavors by conferring exclusive rights. Omitting copyright registration can expose one's work to unauthorized copying, reproduction, or duplication without due credit to the original creator. </p>
<br>
<p align="justify"> What are the rights that the copyright owner get?</p><br>
<p align="justify">The copyright gives exclusive rights to its owner, such as;</p>
<li>The owner has the right to reproduce his work or authorize someone else to do it.</li>
<li>The copyright holder can make his/her work publicly accessible and can also renovate the work to publish in different media forms.</li>
<li>The copyright holder can assign or license the work to a third party with a legal agreement but the ownership remains with the original holder.</li>
<li>The owner can file a legal case and ask for damage fees in case of infringement.</li>
<br>
<p align="justify"> Required Documents:-</p><br>
<li>3 copies of work if it is published. If not published, then 2 copies of the work.</li>
<li>DD or IPO of Rs. (Estimated) depending upon the work.</li>
<li>NOC from different copyright holders (if any) or the agreement.</li>
<li>Information about the title and language of the script.</li>
<li>If the applicant is not an author, give full details (like name, date of birth, address, and so on).</li>
<li>Information regarding the year of publication must be there.</li>
