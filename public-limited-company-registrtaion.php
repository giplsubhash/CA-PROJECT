<p align="justify">In accordance with the regulations outlined in the Companies Act of 2013, a Public Limited company is formally recognized as a corporate entity that holds limited liability and is authorized to issue shares to the general public. As per the Act's provisions, a Public Limited company necessitates a minimum of three directors and a minimum of seven members. It is imperative to maintain the presence of these seven distinct members consistently. Notably, there exists no upper constraint on the permissible count of shareholders for a public limited company.</p>
<br>
<p align="justify">The establishment of a Public Limited company typically entails a significant capital investment. Crucially, the company's name must conclude with the term 'LIMITED'. The registration process entails crucial stages, including the acquisition of a Director Identification Number (DIN) and a Digital Signature Certificate (DSC), validation of the chosen company name, attainment of the incorporation certificate, acquisition of a Permanent Account Number (PAN) and a Tax Deduction and Collection Account Number (TAN), and the initiation of a bank account. To initiate the registration process, it is pivotal to submit an application along with essential documents such as the Memorandum of Association (MOA), Articles of Association (AOA), completed forms DIR-12, INC-7, and INC-22 to the Registrar of Companies (ROC). Furthermore, the requisite registration fees must be duly remitted to the ROC.</p>
