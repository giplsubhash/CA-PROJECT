        <p align="justify">
            Criminal law encompasses a set of legal principles that outline which actions are considered crimes and how the government can prosecute individuals who engage in such unlawful behavior. Federal, state, and local authorities each maintain their penal codes, which outline the specific crimes they prohibit and the corresponding penalties for offenders. Those who violate these laws may be subjected to fines, probation, or imprisonment. The enforcement of criminal law is carried out by prosecuting attorneys representing the government in lawsuits against criminals.
        </p>
        <br>
        <p align="justify">Criminal law governs a range of offenses, including felonies and misdemeanors, which are deemed offenses against the state. The standard of proof required for crimes is "beyond a reasonable doubt." In India, criminal law encompasses offenses against the state and is regulated by statutes such as the Indian Penal Code, Criminal Procedure Code (CrPC), and the Indian Evidence Act.
        </p>
        <br>
        <p align="justify">Criminal law primarily deals with conduct that poses a threat to public safety and welfare, and it establishes punishments for committing such acts. For instance, laws prohibiting murder are part of substantive criminal law, defining the crimes and their corresponding penalties. On the other hand, Criminal Procedure refers to the process through which the government enforces these substantive laws. It involves gathering evidence and prosecuting offenders, which is considered a procedural aspect of criminal law.</p>
