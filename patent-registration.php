<p align="justify">Steps to Get Your Patent Registration:</p><br>
<li>The foremost step is to do a patent search fore-filling up the application. As if the invention is found similar to any earlier arts then it may be challenged by the Indian Patent office.</li><br>
<li>The patent should be written in techno legal language. Give specifications of every detail like the area of the invention and the procedure of how it is made.</li><br>
<li>Then one is able to file an application to the Patent Registration in India. If no issues arise then you have to submit full specifications within 12 months.</li><br>
<li>Now you can advertise your invention in journals and wait if any objection arises. If it happens then the examiner will take suitable steps.</li><br>
<li>If no issues occurred or the examiner gets a valid reason, then the authorized body will grant the patent.</li><br>
