<div class="title-section left">
<p align="justify">Private Limited Company closure in Kolkata involves adhering to a well-defined legal process to ensure compliance with the Companies Act. The closure can be done through voluntary strike-off if the company is inactive or through liquidation in case of outstanding debts. Properly completing all required filings, obtaining approvals from shareholders, and clearing liabilities are crucial steps. It is essential to handle the process with due diligence to avoid penalties or complications. For those seeking reliable support for Private Limited Company closure in Kolkata, our experienced team offers comprehensive guidance to ensure a hassle-free and compliant closure.</p><br>
<p align="justify"><u>There are two ways a Pvt Ltd company can face closure:-</u></p><br>
<li><b>Voluntary Closure:-</b> A Pvt. Ltd. corporation must go through a protracted compliance filing process in order to voluntarily close. The events at issue might include anything, such as the approval of a special resolution by a majority of shareholders or the conclusion of the period for which the business was founded.</li><br>
<li><b>Defunct Winding-Up:-</b> As per the Companies Act, 2013, if a company is declared as a Dormant Company, the government provides certain relief to wind up the company. This requires the filing of the STK-2 form with the Registrar of Companies.</li>
